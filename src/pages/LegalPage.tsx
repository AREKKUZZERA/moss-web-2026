const documentNav = [
  { slug: 'offer', label: 'Оферта' },
  { slug: 'terms', label: 'Соглашение' },
  { slug: 'privacy', label: 'Конфиденциальность' },
  { slug: 'rules', label: 'Правила' },
  { slug: 'contacts', label: 'Контакты' },
];

export function LegalPage() {
  const { slug = 'offer' } = useParams();
  const document = documents[slug];

  if (!document) {
    return <Navigate to="/legal/offer" replace />;
  }

  return (
    <div className="page-stack legal-page">
      <section className="panel legal-hero">
        <span className="crumb">Документы</span>
        <h1>{document.title}</h1>
        <p>{document.lead}</p>
      </section>

      <div className="legal-layout">
        <nav className="panel legal-nav" aria-label="Разделы документов">
          {documentNav.map(({ slug: navSlug, label }) => (
            <Link key={navSlug} to={`/legal/${navSlug}`} className={navSlug === slug ? 'active' : ''}>
              {label}
            </Link>
          ))}
        </nav>

        <article className="panel legal-document">
          {document.sections.map((section) => (
            <section key={section.heading}>
              <h2>{section.heading}</h2>
              <p>{section.body}</p>
            </section>
          ))}
          <div className="legal-note">
            <strong>Правообладатель:</strong> команда default squad
            <br />
            <strong>Разработчик:</strong> arekkuzzera
          </div>
        </article>
      </div>
    </div>
  );
}
